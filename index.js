// Reviews with Data SDK
let reviews = [];
let selectedRating = 5;

function buildStarSelect(){
  const c = document.getElementById('starSelect');
  if (!c) return;
  c.innerHTML = '';
  for(let i=1;i<=5;i++){
    const s = document.createElement('span');
    s.className = 'star text-2xl';
    s.textContent = i <= selectedRating ? '★' : '☆';
    s.style.color = i <= selectedRating ? '#f59e0b' : '#d1d5db';
    s.onclick = () => { selectedRating = i; buildStarSelect(); };
    c.appendChild(s);
  }
}

async function submitReview(){
  const name = document.getElementById('reviewName').value.trim();
  const text = document.getElementById('reviewText').value.trim();
  if(!name || !text) return;
  if(reviews.length >= 999){ document.getElementById('reviewLoading').textContent = 'Лимит отзывов достигнут'; document.getElementById('reviewLoading').classList.remove('hidden'); return; }
  document.getElementById('reviewLoading').classList.remove('hidden');
  const r = await window.dataSdk.create({ name, text, rating: selectedRating, likes: 0, views: 0, created_at: new Date().toISOString() });
  document.getElementById('reviewLoading').classList.add('hidden');
  if(r.isOk){ document.getElementById('reviewName').value=''; document.getElementById('reviewText').value=''; selectedRating=5; buildStarSelect(); }
}

function renderReviews(){
  const c = document.getElementById('reviewsList');
  if (!c) return;
  const existing = new Map([...c.children].map(el=>[el.dataset.rid, el]));
  const ids = new Set();
  reviews.forEach(rev => {
    ids.add(rev.__backendId);
    if(existing.has(rev.__backendId)){
      const el = existing.get(rev.__backendId);
      el.querySelector('.rev-likes').textContent = rev.likes;
      el.querySelector('.rev-views').textContent = rev.views;
    } else {
      const div = document.createElement('div');
      div.dataset.rid = rev.__backendId;
      div.className = 'glass-white rounded-2xl p-5 shadow-sm card-hover';
      div.innerHTML = `<div class="flex justify-between items-start mb-2"><strong class="text-gray-900">${rev.name}</strong><span class="text-amber-400">${'★'.repeat(rev.rating)}${'☆'.repeat(5-rev.rating)}</span></div><p class="text-gray-700 text-sm mb-3">${rev.text}</p><div class="flex gap-4 text-xs text-gray-500"><button onclick="likeReview('${rev.__backendId}')" class="flex items-center gap-1 hover:text-primary transition"><i data-lucide="heart" class="w-3 h-3"></i><span class="rev-likes">${rev.likes}</span></button><span class="flex items-center gap-1"><i data-lucide="eye" class="w-3 h-3"></i><span class="rev-views">${rev.views}</span></span></div>`;
      c.appendChild(div);
      lucide.createIcons();
    }
  });
  existing.forEach((el,id) => { if(!ids.has(id)) el.remove(); });
  document.getElementById('reviewCount').textContent = `${reviews.length} отзыв(ов)`;
}

async function likeReview(id){
  const rev = reviews.find(r=>r.__backendId===id);
  if(rev){ await window.dataSdk.update({...rev, likes: rev.likes+1}); }
}

// Increment views on visible reviews
let viewedIds = new Set();
function incrementViews(){
  reviews.forEach(async rev => {
    if(!viewedIds.has(rev.__backendId)){ viewedIds.add(rev.__backendId); await window.dataSdk.update({...rev, views: rev.views+1}); }
  });
}



// Chatbot
function toggleChat() {
    const chat = document.getElementById('chatbot');

    if (chat.classList.contains('open')) {
        chat.classList.remove('open');
    } else {
        chat.classList.add('open');
    }
}
function addMessage(text, isUser = false) {

    const msgs = document.getElementById('chatMessages');

    const div = document.createElement('div');

    div.className = isUser
        ? 'bg-gray-100 text-sm rounded-2xl p-3 max-w-[85%] ml-auto text-gray-700'
        : 'bg-blue-50 text-sm rounded-2xl p-3 max-w-[85%] text-gray-700';

    div.innerHTML = text;

    msgs.appendChild(div);

    msgs.scrollTop = msgs.scrollHeight;
}

